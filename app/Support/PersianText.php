<?php

namespace App\Support;

/**
 * نرمال‌سازی متن فارسی برای تطبیق.
 *
 * بدون این، «کارکرد» و «كاركرد» دو رشته‌ی متفاوت‌اند (ک و ي عربی در برابر
 * فارسی)، «۱۲» با «12» جور نمی‌شود و نیم‌فاصله هر کلمه را دو تکه می‌کند.
 * کاربر ایرانی هر سه شکل را تایپ می‌کند، پس تطبیق باید هر سه را یکی ببیند.
 */
class PersianText
{
    /** نویسه‌های عربی و هم‌شکل که باید به معادل فارسی برگردند. */
    private const CHARACTER_MAP = [
        'ك' => 'ک', 'ﻙ' => 'ک', 'ﻚ' => 'ک',
        'ي' => 'ی', 'ﻱ' => 'ی', 'ﻲ' => 'ی', 'ى' => 'ی',
        'ة' => 'ه', 'ۀ' => 'ه',
        'ؤ' => 'و', 'إ' => 'ا', 'أ' => 'ا', 'آ' => 'ا', 'ٱ' => 'ا',
        'ئ' => 'ی',
    ];

    /** اعراب و نویسه‌های نامرئی که در تطبیق نقشی ندارند. */
    private const REMOVABLE = [
        "\u{064B}", "\u{064C}", "\u{064D}", "\u{064E}", "\u{064F}", "\u{0650}",
        "\u{0651}", "\u{0652}", "\u{0653}", "\u{0654}", "\u{0655}", "\u{0640}",
        "\u{200C}", "\u{200D}", "\u{200E}", "\u{200F}", "\u{FEFF}",
    ];

    private const DIGIT_MAP = [
        '۰' => '0', '۱' => '1', '۲' => '2', '۳' => '3', '۴' => '4',
        '۵' => '5', '۶' => '6', '۷' => '7', '۸' => '8', '۹' => '9',
        '٠' => '0', '١' => '1', '٢' => '2', '٣' => '3', '٤' => '4',
        '٥' => '5', '٦' => '6', '٧' => '7', '٨' => '8', '٩' => '9',
    ];

    /**
     * شکل یکدست یک متن برای مقایسه.
     *
     * نیم‌فاصله به فاصله تبدیل نمی‌شود بلکه حذف می‌شود، چون «می‌شود» و
     * «میشود» باید یکی باشند نه «می شود».
     */
    public static function normalize(string $text): string
    {
        $text = mb_strtolower(trim($text));
        $text = strtr($text, self::CHARACTER_MAP);
        $text = strtr($text, self::DIGIT_MAP);
        $text = str_replace(self::REMOVABLE, '', $text);

        // نشانه‌گذاری به فاصله تبدیل می‌شود تا کلمه‌ها از هم جدا بمانند
        $text = preg_replace('/[^\p{L}\p{N}]+/u', ' ', $text) ?? $text;

        return trim(preg_replace('/\s+/', ' ', $text) ?? $text);
    }

    /**
     * واژه‌های معنادار متن.
     *
     * کلمات ربطی و پرتکرار حذف می‌شوند چون در همه‌ی جمله‌ها هستند و اگر
     * بمانند، هر پیامی با هر موضوعی امتیاز می‌گیرد.
     *
     * @return array<int,string>
     */
    public static function tokens(string $text): array
    {
        $stopWords = [
            'و', 'در', 'به', 'از', 'که', 'را', 'با', 'این', 'آن', 'است', 'هست',
            'می', 'برای', 'یک', 'شود', 'شد', 'کرد', 'کنم', 'کنید', 'من', 'ما',
            'شما', 'ایا', 'چه', 'چی', 'چطور', 'چگونه', 'ها', 'های', 'تا', 'هم',
            'اگر', 'یا', 'بر', 'کن', 'بود', 'باشد', 'دارد', 'دارم', 'دارید',
        ];

        $tokens = array_filter(
            explode(' ', self::normalize($text)),
            fn (string $t) => mb_strlen($t) > 1 && ! in_array($t, $stopWords, true),
        );

        return array_values(array_unique($tokens));
    }

    /**
     * واژه‌ها به‌علاوه‌ی جفت‌های چسبیده‌ی مجاور.
     *
     * کاربر «ثبت نام» می‌نویسد ولی کلیدواژه «ثبتنام» است؛ بدون ساختن جفت،
     * پیام به دو واژه‌ی بی‌وزن تجزیه می‌شد و موضوع تشخیص داده نمی‌شد. همین
     * برای «از دست»، «کلمه عبور» و «پیام رسان» هم لازم است.
     *
     * @return array<int,string>
     */
    public static function searchTerms(string $text): array
    {
        $tokens = self::tokens($text);
        $terms = $tokens;

        // جفت‌ها از متنِ نرمال‌شده ساخته می‌شوند نه از توکن‌های فیلترشده،
        // چون واژه‌ی ربطی ممکن است وسط یک عبارت باشد («از دست رفتن»).
        $words = array_values(array_filter(explode(' ', self::normalize($text))));

        for ($i = 0; $i < count($words) - 1; $i++) {
            $terms[] = $words[$i].$words[$i + 1];
        }

        return array_values(array_unique($terms));
    }

    /**
     * درجه‌ی تطبیق دو واژه: ۱ برای یکسان، ۰٫۸ برای هم‌ریشه، ۰ برای بی‌ربط.
     *
     * فارسی چسبان است: «پرداخت»، «پرداختی» و «پرداخت‌ها» یک مفهوم‌اند، پس
     * تطبیق پیشوندی لازم است. ولی نباید هم‌وزنِ تطبیق دقیق باشد: «پشتیبانی»
     * با «پشتیبان» (بکاپ) هم‌ریشه است و اگر امتیاز کامل بگیرد، سوالِ تماس با
     * پشتیبانی به موضوع بکاپ می‌رود — دقیقاً همان اشتباهی که رخ می‌داد.
     */
    public static function similarity(string $token, string $keyword): float
    {
        if ($token === $keyword) {
            return 1.0;
        }

        $shorter = mb_strlen($token) <= mb_strlen($keyword) ? $token : $keyword;
        $longer = $shorter === $token ? $keyword : $token;
        $shortLen = mb_strlen($shorter);

        if (! str_starts_with($longer, $shorter)) {
            return 0.0;
        }

        $extra = mb_strlen($longer) - $shortLen;

        /*
         * ریشه‌ی سه‌نویسه‌ای فقط وقتی پذیرفته می‌شود که تفاوت طول کم باشد
         * («امن» با «امنه» بله، «امن» با «امنیتی‌ترین» نه)؛ از چهار نویسه به
         * بالا دست بازتر است.
         */
        if ($shortLen >= 4 && $extra <= 4) {
            return 0.8;
        }

        if ($shortLen === 3 && $extra <= 2) {
            return 0.7;
        }

        return 0.0;
    }
}
